#include <iostream>
using namespace std;

int main()
{
    for (int i = 1; i <= 5; i++)
    {
        cout << i;
    }
    cout << endl;
}

/*

int i = 1; initialization
i<=5; condition
i++  updation

initialization: This part is executed before the loop begins. It is typically used to initialize variables.

condition: The loop continues to execute as long as this condition is true. If the condition evaluates to false initially, the loop will not execute at all.

update: This part is executed after each iteration of the loop. It is generally used to update the loop control variable.

*/